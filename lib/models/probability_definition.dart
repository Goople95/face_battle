/// 概率定义文档
/// 
/// 在AI思考复盘中，所有概率都遵循统一定义：
/// 
/// **概率 = 叫牌为真的概率**
/// 即：全场（10个骰子）确实有这么多个指定点数的概率
/// 
/// 具体应用：
/// 1. 对于叫牌操作（action = bid）：
///    - probability = 我的叫牌成功的概率
///    - 例：叫3个5，probability=0.868 表示全场有至少3个5的概率为86.8%
/// 
/// 2. 对于质疑操作（action = challenge）：
///    - probability = 对手叫牌为真的概率
///    - 例：质疑对手，probability=0.35 表示对手叫牌为真的概率只有35%
///    - 概率越低，质疑越合理
/// 
/// 3. 首轮叫牌：
///    - probability = 基于已知信息（自己的骰子）推算的成功概率
/// 
/// 计算方法：
/// - 使用二项分布计算
/// - 考虑万能牌（1点）的影响
/// - 单个骰子是目标值的概率：
///   - 如果目标是1，或者1已被叫：p = 1/6
///   - 其他情况（1是万能牌）：p = 2/6
/// 
class ProbabilityDefinition {
  static const String definition = "叫牌为真的概率";
}