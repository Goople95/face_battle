/// 骰子游戏规则文档 - 大话骰（吹牛）
/// 
/// 本文档详细说明游戏的完整规则和玩法

class GameRules {
  /// ===========================
  /// 一、游戏基础
  /// ===========================
  /// 
  /// 【游戏名称】
  /// 大话骰（Liar's Dice）/ 骰子吹牛
  /// 
  /// 【游戏人数】
  /// 2人对战（1个玩家 vs 1个AI）
  /// 
  /// 【游戏道具】
  /// - 每人5个骰子，共10个骰子
  /// - 骰子点数：1、2、3、4、5、6
  /// - 每人的骰子对对方保密
  
  /// ===========================
  /// 二、特殊规则 - 万能牌
  /// ===========================
  /// 
  /// 【1点是万能牌】
  /// - 默认情况下，1点可以当作任何点数使用
  /// - 例如：如果你有2个1和1个5，叫"3个5"是合理的
  /// 
  /// 【叫1规则】
  /// - 一旦有人叫了1（如"2个1"），1点就失去万能属性
  /// - 叫1后，1只能当作1使用，不能再当作其他点数
  /// - 这个状态持续到本局结束
  
  /// ===========================
  /// 三、叫牌规则
  /// ===========================
  /// 
  /// 【叫牌含义】
  /// - 叫牌是指猜测全场10个骰子中某个点数的总数量
  /// - 例如："3个5"表示全场10个骰子中至少有3个5
  /// 
  /// 【起叫规则】⭐ 重要
  /// - 起叫（第一次叫牌）最少2个，不能叫1个
  /// - 例如："2个3" ✓ 、"1个6" ✗
  /// 
  /// 【叫牌递增规则】
  /// 每次叫牌必须比上一个叫牌"更高"：
  /// 
  /// 1. 相同点数，增加数量
  ///    - 例：3个4 → 4个4 ✓
  /// 
  /// 2. 更高点数，数量相同或增加
  ///    - 例：3个4 → 3个5 ✓（换高点数，数量不变）
  ///    - 例：3个4 → 4个5 ✓（换高点数，数量增加）
  ///    - 例：3个4 → 2个5 ✗（违规：数量减少）
  /// 
  /// 3. 更低点数，必须增加数量
  ///    - 例：3个5 → 4个2 ✓（换低点数，数量增加）
  ///    - 例：3个5 → 3个2 ✗（违规：数量未增加）
  /// 
  /// 【点数大小顺序】
  /// 2 < 3 < 4 < 5 < 6 < 1
  /// - 1是最大的点数（但通常因为是万能牌不会被叫）
  /// - 2是最小的点数
  /// 
  /// 【特殊规则：叫了1之后换其他数字】⭐ 重要
  /// - 如果上一个叫牌是1（如"2个1"），要换成其他数字必须增加数量
  /// - 例：2个1 → 2个2 ✗（必须增加数量）
  /// - 例：2个1 → 3个2 ✓（数量增加了）
  /// - 例：2个1 → 3个1 ✓（继续叫1可以）
  
  /// ===========================
  /// 四、游戏流程
  /// ===========================
  /// 
  /// 【开始游戏】
  /// 1. 双方各摇5个骰子，查看自己的点数
  /// 2. 系统随机决定谁先叫牌
  /// 
  /// 【叫牌阶段】
  /// 1. 先手玩家进行第一次叫牌
  /// 2. 双方轮流叫牌，每次必须比对方更高
  /// 3. 持续进行直到有人质疑
  /// 
  /// 【质疑阶段】
  /// 当一方认为对手的叫牌不真实时，可以选择质疑：
  /// 1. 质疑后，双方亮出所有骰子
  /// 2. 统计全场该点数的实际数量
  /// 3. 判定胜负
  
  /// ===========================
  /// 五、胜负判定
  /// ===========================
  /// 
  /// 【质疑结果】
  /// - 如果实际数量 ≥ 叫牌数量：叫牌为真，质疑者输
  /// - 如果实际数量 < 叫牌数量：叫牌为假，叫牌者输
  /// 
  /// 【示例】
  /// - A叫"5个4"，B质疑
  /// - 亮骰后统计：全场共有6个4
  /// - 6 ≥ 5，叫牌为真，B（质疑者）输
  
  /// ===========================
  /// 六、惩罚机制 - 喝酒系统
  /// ===========================
  /// 
  /// 【输局惩罚】
  /// - 输掉一局的玩家/AI需要喝一杯酒
  /// - 累计喝酒数量会影响状态
  /// 
  /// 【醉酒状态】
  /// - 0-2杯：清醒状态，可以正常游戏
  /// - 3-5杯：微醺状态，不能继续游戏
  /// - 6杯：醉酒状态，完全不能游戏
  /// 
  /// 【醒酒机制】
  /// - 每10分钟自动减少1杯酒
  /// - 倒计时显示在界面上
  /// - 减到2杯以下可以重新开始游戏
  
  /// ===========================
  /// 七、AI系统
  /// ===========================
  /// 
  /// 【AI角色】
  /// 游戏提供多个AI角色，每个有不同性格：
  /// - 冲动小哥：激进、爱冒险
  /// - 保守型：谨慎、稳健
  /// - 诚实型：很少虚张
  /// - 狡猾型：善于欺骗
  /// 
  /// 【AI决策】
  /// - 使用Gemini AI进行智能决策
  /// - 降级时使用本地概率计算
  /// - AI会考虑玩家的历史行为数据
  
  /// ===========================
  /// 八、策略提示
  /// ===========================
  /// 
  /// 【基础策略】
  /// 1. 利用万能牌：1点的万能属性很重要
  /// 2. 观察对手：记录对手的叫牌习惯
  /// 3. 适度虚张：完全诚实容易被预测
  /// 4. 把握时机：选择合适的时机质疑
  /// 
  /// 【高级策略】
  /// 1. 诱导策略：故意示弱引诱对手质疑
  /// 2. 心理战：通过表情和对话影响对手
  /// 3. 概率计算：基于二项分布估算可能性
  /// 4. 模式识别：发现对手的行为模式
  
  /// ===========================
  /// 九、界面操作
  /// ===========================
  /// 
  /// 【叫牌操作】
  /// 1. 选择数量：滑动或点击调整
  /// 2. 选择点数：点击骰子图标
  /// 3. 确认叫牌：点击"叫牌"按钮
  /// 
  /// 【质疑操作】
  /// - 点击"开"按钮进行质疑
  /// - 质疑后自动显示结果
  /// 
  /// 【查看信息】
  /// - 点击AI头像查看其行为分析
  /// - 游戏结束后显示详细统计
}